      <section class="sec02 section" id="about-sle">
        <h2 class="about-sle__title">
          SLEとは
        </h2>
        <p class="about-sle__text">
          全身性エリテマトーデスは、細菌やウイルスと戦う免疫機能が異常をきたし、自分自身の身体を傷つけてしまう「<ruby>膠原病<rt>こうげんびょう</rt></ruby>」の一つです。<br>
          英語の病名 Systemic Lupus Erythematosus の頭文字をとり <span class="under_line"><ruby>SLE<rt>エスエルイー</rt></ruby>と呼ばれます。</span><br>
          身体のさまざまな場所・臓器に症状があらわれることから Systemic（全身）、オオカミに噛まれたような、赤い紅斑（腫れ）が頬にできることから Lupus（ラテン語でオオカミ）、Erythematosus（紅斑）と命名されています。<br>
          全身性と名前の通り 特徴的に関節、皮膚、腎臓、血管、神経など全身の臓器に障害を起こしやすいです。
        </p>
      </section>
      
      <section class="section" id="early-symptoms">
