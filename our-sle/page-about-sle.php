        <h3 class="about-sle_sub-title">・発症年齢</h3>
        <p class="about-sle__text">
          10代から70歳以上まで、幅広い年齢の患者さんが治療を受けられています。<br>
          発症年齢も幅広いですが、特に妊娠や出産が可能な20～40代の女性に多いとされています。
        </p>
      </section>
      
      <section class="section" id="etiology">
        <h2 class="about-sle__title">
          SLEの原因
        </h2>
        <p class="about-sle__text">
          <span class="under_line">はっきりした原因はわかっていません。</span><br>
          発症に関わる遺伝的要因（遺伝子）それに加えてなんらかの環境要因が複合的に関わって発症すると考えられています。<br>
          なんらかのきっかけや誘因には大きく分けて４つあり複雑に組み合わさっていると考えられています。
        </p>
        <h3 class="about-sle_sub-title">・環境要因</h3>
        <p class="about-sle__text">
          紫外線に浴びたり、喫煙、ウイルスや細菌感染、寒冷刺激、外傷、手術、妊娠・出産、薬剤、ストレス、特定の薬、ケガや手術が想定されています。
        </p>
        <h3 class="about-sle_sub-title">・遺伝的要因</h3>
        <p class="about-sle__text">
          病気と関連する遺伝子が幾つか報告されています。
        </p>
        <h3 class="about-sle_sub-title">・免疫の異常</h3>
        <p class="about-sle__text">
          自分の体を免疫が攻撃してしまう。
        </p>
        <h3 class="about-sle_sub-title">・性ホルモン</h3>
        <p class="about-sle__text">
          発症時期が20〜30代が多いことから性ホルモンが影響されていると考えられてます。<br>
          日光に長時間にさらされることで発症のきっかけになることもあり、たばこを吸う人は吸わない人よりも発症率が高くなっています。
        </p>
      </section>
      
      <section class="section" id="immune-dysfunction">
        <h2 class="about-sle__title">
          免疫の異常とは
        </h2>
        <p class="about-sle__text">
          <span class="under_line">自分の体を自分の免疫が攻撃してしまう</span>、自己免疫反応によりさまざまな炎症が起こります。<br>
          抗体はウイルスや細菌などにくっついて捕まえ、体を守ります。<br>
          SLEなどの自己免疫疾患では血液中に抗核抗体があると、自分の細胞内の核の成分を攻撃し、くっついて免疫複合体を作ります。免疫複合体が血液に乗って体内を流れ、腎臓や皮膚などにくっついたり、たまったりして炎症が起こます。
        </p>
      </section>
      
      <section class="sec06 section" id="long-term-prognosis">
        <h2 class="about-sle__title">
          長期的な経過
        </h2>
        <p class="about-sle__text">
          SLEは、症状が落ち着く「寛解」と、症状が強くなる「再燃」をくり返しながら経過していく病気です。
          寛解のあいだは、体のつらさが和らぎ、ふだんの生活を送りやすくなります。一方で、再燃すると発熱・関節の痛み・強い疲れなどの症状があらわれ、治療の見直しが必要になることもあります。<br>
          この「波のある経過」は、SLEの大きな特徴のひとつです。再燃がいつ起きるかを完全に予測することはむずかしく、そのことが患者さんや家族にとって不安の種になることも少なくありません。<br>
          ただ、治療薬の進歩により、寛解を長く保ちやすくなってきています。定期的な通院と主治医との連携を続けることが、再燃を早めに気づき、対処するための大きな力になります。<br>
          SLEとの生活は、長い旅のようなものです。波があることを知り、自分の体のサインに少しずつ気づいていくことが、毎日を自分らしく過ごすことにつながっていきます。
        </p>
      </section>
      
